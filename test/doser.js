module.exports = {
  Create: function (n) {
    n.click('a#tab-doser')
      .wait(500)
      .click('input#add_doser')
      .wait(500)
      .type('input#doser_name', 'Two Part - CaCO3')
      .wait(500)
      .click('button#new_doserjack')
      .wait(500)
      .click('span#new_doser-J0')
      .wait(500)
      .click('input#create_pump')
      .wait(500)
      .click('input#schedule-pump-1')
      .wait(500)
      .click('input#pump-enable-1')
      .wait(500)
      .type('input#doser-1-day')
      .type('input#doser-1-day', '*')
      .wait(500)
      .type('input#doser-1-hour')
      .type('input#doser-1-hour', '1,9,17')
      .wait(500)
      .type('input#doser-1-minute')
      .type('input#doser-1-minute', '1')
      .wait(500)
      .type('input#doser-1-second')
      .type('input#doser-1-second', '1')
      .wait(500)
      .type('input#set-duration-1')
      .type('input#set-duration-1', '15')
      .wait(500)
      .click('input#set-schedule-1')
      .wait(1500)

    return function () {
      return ('Doser created')
    }
  }
}
